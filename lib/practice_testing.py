def divisible_by_three(number):
    if number % 3 == 0:
        return True
    else:
        return False

def divisible_by_five(number):
    if number % 5 == 0:
        return True
    else:
        return False
    
def add(num1, num2):
    return num1 + num2

def subtract(num1, num2):
    return num1 - num2

def multiply_number_string(string1, string2):
    return int(string1) * int(string2)